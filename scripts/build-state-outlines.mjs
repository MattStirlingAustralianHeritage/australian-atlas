#!/usr/bin/env node

/**
 * Build lib/regions/stateOutlines.js — simplified SVG silhouettes of the
 * 8 Australian states/territories for the /regions index cards.
 *
 * Input: a states GeoJSON (default /tmp/au-states.geojson, from
 * https://github.com/rowanhogan/australian-states). Rings are
 * Douglas-Peucker-simplified and tiny islands dropped, then projected to a
 * per-state viewBox (width 100, height preserving aspect at the state's
 * mid-latitude). The generated module is committed — this script only needs
 * re-running if the silhouettes ever change.
 *
 * Usage: node scripts/build-state-outlines.mjs [path-to-geojson]
 */

import { readFileSync, writeFileSync } from 'fs'

const SRC = process.argv[2] || '/tmp/au-states.geojson'

const NAME_TO_CODE = {
  'New South Wales': 'NSW', 'Victoria': 'VIC', 'Queensland': 'QLD',
  'South Australia': 'SA', 'Western Australia': 'WA', 'Tasmania': 'TAS',
  'Northern Territory': 'NT', 'Australian Capital Territory': 'ACT',
}

// ── Douglas-Peucker on [lng,lat] rings ──
function perpDist(p, a, b) {
  const dx = b[0] - a[0], dy = b[1] - a[1]
  const len2 = dx * dx + dy * dy
  if (len2 === 0) return Math.hypot(p[0] - a[0], p[1] - a[1])
  let t = ((p[0] - a[0]) * dx + (p[1] - a[1]) * dy) / len2
  t = Math.max(0, Math.min(1, t))
  return Math.hypot(p[0] - (a[0] + t * dx), p[1] - (a[1] + t * dy))
}

function dp(points, tol) {
  if (points.length <= 3) return points
  let maxD = 0, idx = 0
  for (let i = 1; i < points.length - 1; i++) {
    const d = perpDist(points[i], points[0], points[points.length - 1])
    if (d > maxD) { maxD = d; idx = i }
  }
  if (maxD <= tol) return [points[0], points[points.length - 1]]
  const left = dp(points.slice(0, idx + 1), tol)
  const right = dp(points.slice(idx), tol)
  return left.slice(0, -1).concat(right)
}

function ringArea(ring) {
  let s = 0
  for (let i = 0; i < ring.length - 1; i++) {
    s += ring[i][0] * ring[i + 1][1] - ring[i + 1][0] * ring[i][1]
  }
  return Math.abs(s / 2)
}

const geo = JSON.parse(readFileSync(SRC, 'utf8'))
const out = {}

for (const f of geo.features) {
  const code = NAME_TO_CODE[f.properties.STATE_NAME]
  if (!code) continue
  const polys = f.geometry.type === 'MultiPolygon' ? f.geometry.coordinates : [f.geometry.coordinates]
  // outer rings only, ranked by area
  const rings = polys.map(p => p[0]).map(r => ({ r, a: ringArea(r) })).sort((x, y) => y.a - x.a)
  const maxA = rings[0].a
  const kept = rings.filter((x, i) => i === 0 || (x.a >= maxA * 0.01 && i < 6)).map(x => x.r)

  // bbox over kept rings
  let minLng = Infinity, maxLng = -Infinity, minLat = Infinity, maxLat = -Infinity
  for (const ring of kept) for (const [lng, lat] of ring) {
    if (lng < minLng) minLng = lng
    if (lng > maxLng) maxLng = lng
    if (lat < minLat) minLat = lat
    if (lat > maxLat) maxLat = lat
  }
  const midLat = (minLat + maxLat) / 2
  const cos = Math.cos((midLat * Math.PI) / 180)
  const lngSpan = maxLng - minLng, latSpan = maxLat - minLat
  const W = 100
  const H = Math.round(W * (latSpan / (lngSpan * cos)) * cos * 100) / 100 // = W * latSpan/lngSpan / cos... simplify below

  // Correct aspect: x spans lngSpan*cos "distance units", y spans latSpan units.
  const height = Math.round((W * latSpan / (lngSpan * cos)) * 100) / 100

  const tol = Math.max(0.02, Math.max(lngSpan, latSpan) / 140)
  const px = (lng) => ((lng - minLng) / lngSpan) * W
  const py = (lat) => ((maxLat - lat) / latSpan) * height

  let d = ''
  let pts = 0
  for (const ring of kept) {
    const simp = dp(ring, tol)
    if (simp.length < 4) continue
    pts += simp.length
    d += simp.map(([lng, lat], i) =>
      `${i === 0 ? 'M' : 'L'}${px(lng).toFixed(1)} ${py(lat).toFixed(1)}`
    ).join('') + 'Z'
  }

  out[code] = { d, w: W, h: height, minLng, maxLng, minLat, maxLat }
  console.log(code, 'rings', kept.length, 'pts', pts, 'viewBox 0 0', W, height, 'dLen', d.length)
}

const module_ = `// GENERATED by scripts/build-state-outlines.mjs — do not hand-edit.
// Simplified state silhouettes for the /regions index cards. Each entry is an
// SVG path in a (0 0 w h) viewBox, plus the geographic bbox used to project
// points into it via projectPoint().

export const STATE_OUTLINES = ${JSON.stringify(out, null, 2)}

// Project a lat/lng into the state's silhouette viewBox coordinates.
// Returns null when the state is unknown or the point is outside the bbox
// (with a small tolerance so coastal centroids never vanish).
export function projectPoint(state, lat, lng) {
  const o = STATE_OUTLINES[state]
  if (!o || lat == null || lng == null) return null
  const x = ((lng - o.minLng) / (o.maxLng - o.minLng)) * o.w
  const y = ((o.maxLat - lat) / (o.maxLat - o.minLat)) * o.h
  if (x < -5 || x > o.w + 5 || y < -5 || y > o.h + 5) return null
  return { x: Math.round(x * 10) / 10, y: Math.round(y * 10) / 10 }
}
`
writeFileSync('lib/regions/stateOutlines.js', module_)
console.log('\nwrote lib/regions/stateOutlines.js')
